    public void formatearCombos() {

        jComboBoxCliente.setRenderer(new DefaultListCellRenderer() {

            @Override
            public Component getListCellRendererComponent(
                    JList list, Object value, int index,
                    boolean isSelected, boolean cellHasFocus) {
                super.getListCellRendererComponent(list, value, index,
                        isSelected, cellHasFocus);

                if (value != null) {
                    Cliente cliente = (Cliente) value;
                    setText(cliente.getNombre());
                }
                return this;
            }
        });
    }

    private void llenarComboClientes() {

        try {
            //llenamos el conboBox de las clientes existentes
           jComboBoxCliente.removeAllItems();

            Cliente cliente0 = new Cliente(0, "Selecciona Cliente", "", "");
            jComboBoxCliente.addItem(cliente0);

            Connection con = conexion();

            List<Cliente> clientes = selectClientes(con);
            for (Cliente p : clientes) {
                jComboBoxCliente.addItem(p);
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(JFrameTendas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

