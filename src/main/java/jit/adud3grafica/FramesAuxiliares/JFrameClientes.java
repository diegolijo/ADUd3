    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Connection con = conexion();
            String nome = jTextFieldNome.getText();
            String apellidos = jTextFieldNome1.getText();
            String email = jTextFieldNome2.getText();

            if ("".equals(nome) || "".equals(apellidos) || "".equals(email) || "Nome".equals(nome) || "Apellidos".equals(apellidos) || "email".equals(email)) {
                JOptionPane.showMessageDialog(null, "Informa todolos campos", "Aviso", 2);
            } else {
                Cliente cliente = new Cliente(0, nome, apellidos, email);
                insertCliente(con, cliente);
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(JFrameClientes.class.getName()).log(Level.SEVERE, null, ex);
        }

        llenarComboClientes();

    }//GEN-LAST:event_jButton1ActionPerformed

